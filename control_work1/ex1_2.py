# Дано:
# 4 введённых числа
# Найти:
# максимальное число = ?
# Решение:
a = float(input('Введите первое число:'))           # вводим 1 - ое число
b = float(input('Введите второе число:'))           # вводим 2 - ое число
c = float(input('Введите третье число:'))           # вводим 3 - е число
d = float(input('Введите четвёртое число:'))        # вводим 4 - ое число
print('максимальное число: ',(max(a,b,c,d)))        # выводим наибольшее число из данных
